    constructor(tradeableItem: Int, quantity: Long, vararg untradableItems: Int) : this(
        tradeableItem,
        false,
        quantity,
        *untradableItems
    )

    constructor(tradeableItem: Int, vararg untradableItems: Int) : this(tradeableItem, 1L, *untradableItems)

    companion object {
        private val MAPPINGS: Multimap<Int, ItemMapping> = HashMultimap.create()

        init {
            for (item in values()) {
                for (itemId in item.untradableItems) {
                    if (item.untradeable) {
                        for (variation in ItemVariationMapping.getVariations(itemId)) {
                            MAPPINGS.put(variation, item)
                        }
                    }
                    MAPPINGS.put(itemId, item)
                }
            }
        }

        /**
         * Get collection of items that are mapped from single item id.
         *
         * @param itemId the item id
         * @return the collection
         */
        fun getMapping(itemId: Int): Collection<ItemMapping>? {
            val mapping = MAPPINGS[itemId]
            return if (mapping.isEmpty()) {
                null
            } else mapping
        }

        fun isMapped(itemId: Int): Boolean {
            return MAPPINGS.containsValue(itemId)
        }
    }
